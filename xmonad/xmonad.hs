myStartupHook = do
  spawnOnce "nitrogen --restore &"
  spawnOnce "pasystray &"
  spawnOnce "blueman-applet &"
  spawnOnce "nm-applet --sm-disable --indicator &"
  spawnOnce "systemctl --user restart polybar.service"

